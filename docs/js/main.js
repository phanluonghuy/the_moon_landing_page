// TEXT TYPING EFFECT (Hero subtitle)
// ============================================
function initTypingEffect() {
  const el = document.querySelector('.hero-subtitle');
  if (!el) return;

  const text = translations[currentLang].hero_subtitle;
  el.textContent = '';
  el.style.opacity = 1;

  let i = 0;
  function type() {
    if (i < text.length) {
      el.textContent += text.charAt(i);
      i++;
      setTimeout(type, 18);
    }
  }

  // Start after loader
  setTimeout(type, 2200);
}

