// I18N ENGINE
// ============================================
let currentLang = localStorage.getItem('portfolio-lang') || 'en';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('portfolio-lang', lang);
  document.documentElement.lang = lang;

  const dict = translations[lang];
  if (!dict) return;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] === undefined) return;

    if (el.getAttribute('data-i18n-html') === 'true') {
      el.innerHTML = dict[key];
    } else {
      el.textContent = dict[key];
    }
  });

  // Update button text to show the OTHER language (what you'll switch to)
  const nextLang = lang === 'en' ? 'VI' : 'EN';
  document.querySelectorAll('.lang-code').forEach((el) => (el.textContent = nextLang));

  // Re-run typing effect for the subtitle when language changes
  const subtitle = document.querySelector('.hero-subtitle');
  if (subtitle && document.querySelector('.loader.hidden')) {
    const text = dict.hero_subtitle;
    subtitle.textContent = '';
    let i = 0;
    function type() {
      if (i < text.length) {
        subtitle.textContent += text.charAt(i);
        i++;
        setTimeout(type, 12);
      }
    }
    type();
  }
}

function toggleLanguage() {
  const next = currentLang === 'en' ? 'vi' : 'en';
  setLanguage(next);
}

function initI18n() {
  // Bind all switcher buttons
  document.querySelectorAll('.lang-switcher').forEach((btn) => {
    btn.addEventListener('click', toggleLanguage);
  });

  // Apply saved or default language
  setLanguage(currentLang);
}

