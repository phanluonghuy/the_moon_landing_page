// CUSTOM CURSOR
// ============================================
function initCursor() {
  const dot = document.querySelector('.cursor-dot');
  const ring = document.querySelector('.cursor-ring');

  if (!dot || !ring || window.matchMedia('(hover: none)').matches) return;

  let mouseX = 0,
    mouseY = 0;
  let ringX = 0,
    ringY = 0;

  document.addEventListener('mousemove', (e) => {
    mouseX = e.clientX;
    mouseY = e.clientY;
    dot.style.left = mouseX - 4 + 'px';
    dot.style.top = mouseY - 4 + 'px';
  });

  function animateRing() {
    ringX += (mouseX - ringX) * 0.12;
    ringY += (mouseY - ringY) * 0.12;
    ring.style.left = ringX - 18 + 'px';
    ring.style.top = ringY - 18 + 'px';
    requestAnimationFrame(animateRing);
  }
  animateRing();

  // Scale on hoverable elements
  const hoverTargets = document.querySelectorAll('a, button, .project-card, .tool-card, .about-card, .client-tag');
  hoverTargets.forEach((el) => {
    el.addEventListener('mouseenter', () => {
      ring.style.transform = 'scale(1.8)';
      ring.style.borderColor = 'rgba(221, 161, 94, 0.6)';
      dot.style.transform = 'scale(0.5)';
    });
    el.addEventListener('mouseleave', () => {
      ring.style.transform = 'scale(1)';
      ring.style.borderColor = 'rgba(221, 161, 94, 0.4)';
      dot.style.transform = 'scale(1)';
    });
  });
}

